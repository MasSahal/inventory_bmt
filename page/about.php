                            <p>Sejarah BMT AL ISHLAH
                            BMT Al-Ishlah adalah unit usaha yang ke-3 yang didirikan Kopontren Al-Ishlah.
                            Berdiri pada tanggal 17 Oktober 1998 atas inisiatif pengurus yang ketika itu dipimpin oleh Bapak Usep Syaifuddin Zuhri.
                            Berkantor di pojok perkantoran pusat bisnis Kopontren berukuran 3 x 3 m2, BMT memulai kegiatan usaha simpan pinjamnya bersebelahan dengan Swamitra yang didukung penuh oleh Bank Bukopin 
                            Dimulai dengan 3 orang karyawan dengan modal awal Rp 10.000.000,- BMT mulai memberikan pembiayaan kepada para pengusaha mikro.
                            Kerja keras dan berbagai upaya dilakukan oleh pengelola BMT mengantarkan BMT memiliki jumlah mitra usaha yang cukup banyak dan memiliki asset pada tahun 2005 sebesar kurang lebih Rp 400.000.000,-.
                            Akan tetapi perjalanan panjang dan kerja keras yang telah dilakukan oleh para pengelola ternyata harus menghadapi kenyataan pahit, ketika terjadi missmanagement yang mengakibatkan asset BMT turun kembali menjadi sekitar Rp 200.000.000,- dengan tingkat NPF lebih dari 85%.
                            Pengurus mengadakan perombakan dan menyisakan 2 orang karyawan untuk melanjutkan pengelolaan BMT.
                            Setelah terjadi penggantian pengurus pada tahun 2006, pengurus baru yang ketika itu di pimpin oleh H. Hasyim Fadjazi mengirimkan 8 orang alumni Al Ishlah untuk belajar pengelolaan BMT ke BMT Al Falah Sumber.
                            Setelah selesai, 8 orang tersebut dengan dibantu seorang konsultan diminta pengurus untuk mengelola BMT Al Ishlah.
                            Babak baru BMT Al Ishlah dimulai pada tanggal 18 Juli 2006.
                            BMT langsung menggunakan software IBSS (Integrated Micro Banking System Sharia) guna mendukung sistem administrasi keuangannya.
                            Pengurus menyulap garasi dan dapur kotor Kopontren menjadi ruang pelayanan, ruang teller dan ruang back office.
                            BMT mulai merubah paradigma usaha menjadi lebih Islami dan pengelolaan organisasi yang lebih profesional.
                            Kerja keras pengelola yang sudah ada ditambah 4 orang baru yang bergabung (hasil pelatihan dan magang di BMT Al Falah) sedikit demi sedikit menunjukkan hasil yang positif
                            Berturut-turut pada akhir tahun 2006 asset BMT naik menjadi Rp 326.000.000,-, tahun 2007 Rp 949.000.000,- dan tahun 2008 Rp 1,3M,-, tahun 2009 Rp 1,6M,-.
                            Jumlah aset dan karyawan terus bertambah dari tahun ke tahun hingga saat ini </p>
